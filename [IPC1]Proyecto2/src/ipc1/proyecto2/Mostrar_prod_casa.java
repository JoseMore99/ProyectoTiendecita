    public Mostrar_prod_casa() {
        initComponents();
        lblwelcom.setText("Hola, "+Login.Nameusu);
        
        modelo = new DefaultTableModel();
        modelo.addColumn("Producto");
        modelo.addColumn("Descripcion");
        modelo.addColumn("Precio");
        
        this.tablap.setModel(modelo);
        
        NodoProducto aux = Productos.LCP.inicio;
        NodoOferta of;
        
        double normal;
        
        for (int i = 0; i < Productos.LCP.size; i++) {
            
            normal=aux.producto.precio;
            of = Ofertas.LCO.cabeza;
            while(of != null){
                
                if(of.Oferta.idProd==aux.producto.id){
                    normal = normal - (normal*of.Oferta.descuento)/100;
                    System.out.println(normal);
                }
                of = of.siguiente;
            }
            
            
            modelo.addRow(new Object[]{aux.producto.nombre,aux.producto.descripcion,normal});
            aux = aux.siguiente;
        }
    }

